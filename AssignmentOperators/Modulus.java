package AssignmentOperators;

public class Modulus {
    public static void main(String[] args) {
        double num1 = 7;
        double num2 = 5;

        double result;

        //Modulus/Remainder
        result = num1 % num2;  //Simple Modulus/Remainder
        System.out.println("Modulus/Remainder:" + result);
    }
}
